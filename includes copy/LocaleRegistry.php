    public static function all(): array
    {
        return self::LOCALES;
    }

    public static function get(string $locale): ?array
    {
        return self::LOCALES[$locale] ?? null;
    }

    public static function fakerLocale(string $locale): string
    {
        return self::LOCALES[$locale][0] ?? 'en_US';
    }

    public static function isFakerNative(string $locale): bool
    {
        return in_array(self::fakerLocale($locale), self::FAKER_NATIVE, true);
    }

    /** Convert a country-name slug like "united-states" to a locale code */
    public static function toSlug(string $locale): string
    {
        $name = self::LOCALES[$locale][1] ?? $locale;
        $slug = strtolower(preg_replace('/[^a-z0-9]+/i', '-', $name));
        return trim(preg_replace('/-+/', '-', $slug), '-'); // collapse double dashes
    }

    public static function fromSlug(string $slug): ?array
    {
        foreach (self::LOCALES as $code => $info) {
            $localeSlug = strtolower(preg_replace('/[^a-z0-9]+/i', '-', $info[1]));
            $localeSlug = trim(preg_replace('/-+/', '-', $localeSlug), '-'); // same collapse
            if ($localeSlug === $slug) {
                return array_merge(['locale' => $code], [
                    'faker_locale' => $info[0],
                    'english_name' => $info[1],
                    'native_name'  => $info[2],
                    'country_code' => $info[3],
                    'flag'         => $info[4],
                ]);
            }
        }
        return null;
    }

    /** Search locales by query */
    public static function search(string $query): array
    {
        $q = strtolower($query);
        return array_filter(self::LOCALES, function ($info) use ($q) {
            return str_contains(strtolower($info[1]), $q)
                || str_contains(strtolower($info[2]), $q)
                || str_contains(strtolower($info[3]), $q);
        });
    }

    /** Group locales by first letter of English name */
    public static function grouped(): array
    {
        $groups = [];
        foreach (self::LOCALES as $code => $info) {
            $letter = strtoupper($info[1][0]);
            $groups[$letter][$code] = $info;
        }
        ksort($groups);
        return $groups;
    }
